def _provenance_header(spec: IntentSpec, extra: list[str] | None = None) -> list[str]:
    lines = [
        "Generated by BP2UIP; do not edit by hand. Every statement in this",
        "document derives from the parsed source, the reviewed intent spec,",
        "or the uplift analysis, and the provenance log next to it records",
        "how it was produced.",
        "",
        f"- Spec: `{spec.spec_id}` (**{spec.status}**)",
    ]
    if spec.approval is not None:
        lines.append(
            f"- Approved by: **{spec.approval.approved_by}** at {spec.approval.approved_at}"
        )
    else:
        lines.append("- Approval: none; this document was force-generated from a draft")
    lines += [
        f"- Extracted by: {spec.extraction.provider}/{spec.extraction.model} "
        f"(prompt v{spec.extraction.prompt_version})",
        f"- Source estate: `{spec.estate_ref.path}` (sha256 `{spec.estate_ref.sha256[:12]}...`)",
    ]
    lines += extra or []
    return lines


def _stage_detail(stage: Stage) -> str:
    props = stage.properties
    if stage.type == "decision":
        return f"`{props.get('expression', '')}`"
    if stage.type in ("action",):
        return f"{props.get('object', '?')} :: {props.get('action', '?')}"
    if stage.type in ("queue_read", "queue_write"):
        queue = props.get("queue_name")
        action = props.get("action", "")
        return f"{action}" + (f" on {queue}" if queue else " (current item)")
    if stage.type == "calculation":
        expression = props.get("expression")
        if expression:
            return f"`{expression}`"
        steps = props.get("steps") or []
        return f"{len(steps)} assignment(s)" if steps else ""
    if stage.type == "exception":
        return f"{props.get('exception_type', 'exception')}: {props.get('detail', '')}"
    if stage.type == "loop_start":
        return f"for each row in `{props.get('collection', '?')}`"
    if stage.type == "subprocess_call":
        return str(props.get("process", ""))
    return ""


def _flow_walk(process: Process) -> list[tuple[Stage, str]]:
    """Stages in depth-first link order from the start stage, true
    branches first, each paired with a branch annotation. Stages not
    reachable from start (recovery chains) follow, marked as such."""
    stages = {s.id: s for s in process.stages}
    outgoing: dict[str, list[tuple[str | None, str]]] = {}
    for link in process.links:
        outgoing.setdefault(link.from_stage, []).append((link.label, link.to_stage))
    for targets in outgoing.values():
        # true before false before unlabeled order is stable for reading
        targets.sort(key=lambda t: {"true": 0, "false": 1}.get(t[0] or "", 2))

    ordered: list[tuple[Stage, str]] = []
    visited: set[str] = set()

    def visit(stage_id: str, annotation: str) -> None:
        if stage_id in visited or stage_id not in stages:
            return
        visited.add(stage_id)
        ordered.append((stages[stage_id], annotation))
        for label, target in outgoing.get(stage_id, []):
            branch = f"on {label} from '{stages[stage_id].name}'" if label else ""
            visit(target, branch)

    for stage in process.stages:
        if stage.type == "start":
            visit(stage.id, "")
    for stage in process.stages:
        if stage.id not in visited:
            visit(stage.id, "exception recovery path")
    return ordered


