"""Document generation: PDD, SDD, and the modernization report.

Implemented in roadmap week 5. Markdown is the primary output; DOCX is
produced via pandoc when it is installed, and skipped with an honest
notice when it is not. Every generator that consumes a spec passes
through the review gate before doing anything else; the gate call is
the first line, not a decoration.

Generation is a pure mapping from the artifacts to a document, no LLM
involved: every statement in a generated document traces to the estate
model, the approved spec, or the uplift report. Where a document needs
something the source cannot provide (selectors, credentials, agentic
design decisions), it says so in an explicit TODO instead of guessing.
"""

import shutil
import subprocess
from dataclasses import dataclass
from pathlib import Path

from bp2uip.analysis import build_dependency_graph, config_constants, score_complexity
from bp2uip.gate import require_approved
from bp2uip.model import (
    ComplexityScore,
    Estate,
    IntentSpec,
    Process,
    ProcessDependencies,
    Stage,
    UpliftReport,
)
from bp2uip.provenance import ProvenanceLog
from bp2uip.render import _cites, _stage_index

ACTOR = "bp2uip/docsgen"


@dataclass
class GeneratedDoc:
    markdown_path: Path
    docx_path: Path | None = None


class GenerationError(Exception):
    """The inputs do not line up (missing process, mismatched spec)."""


def _find_process(estate: Estate, process_id: str) -> Process:
    process = next((p for p in estate.processes if p.id == process_id), None)
    if process is None:
        raise GenerationError(f"process {process_id} is not in the estate")
    return process


def _try_docx(markdown_path: Path) -> Path | None:
    """Convert the markdown to DOCX when pandoc is available. Returns
    the DOCX path, or None (honestly, not a fake) when it is not."""
    if shutil.which("pandoc") is None:
        return None
    docx_path = markdown_path.with_suffix(".docx")
    subprocess.run(
        ["pandoc", str(markdown_path), "-o", str(docx_path)],
        check=True,
        capture_output=True,
    )
    return docx_path


def _write(out_dir: Path, name: str, content: str) -> GeneratedDoc:
    out_dir.mkdir(parents=True, exist_ok=True)
    markdown_path = out_dir / name
    markdown_path.write_text(content, encoding="utf-8")
    return GeneratedDoc(markdown_path=markdown_path, docx_path=_try_docx(markdown_path))


def _record_generation(log: ProvenanceLog, kind: str, spec: IntentSpec, doc: GeneratedDoc) -> None:
    log.append(
        actor=ACTOR,
        event="generation",
        detail={"kind": kind, "spec_id": spec.spec_id, "path": str(doc.markdown_path)},
    )


