def _target_architecture(deps: ProcessDependencies, couplings: list, process: Process) -> list[str]:
    reads = [q for q in deps.queues if q.reads]
    adds = [q for q in deps.queues if q.adds]
    lines: list[str] = []
    if reads:
        names = ", ".join(q.queue for q in reads)
        lines.append(
            f"- **REFramework transactional process (performer)**: the process "
            f"works items from {names}, which maps directly to the REFramework "
            f"transaction loop over an Orchestrator queue."
        )
    if adds:
        names = ", ".join(q.queue for q in adds)
        lines.append(
            f"- **Dispatcher**: the process adds items to {names}; in UiPath "
            f"this becomes a dispatcher feeding the Orchestrator queue."
        )
    if not reads and not adds:
        lines.append(
            "- **Sequential workflow**: no queue interaction in the source; a "
            "linear workflow (REFramework optional) invoked with arguments is "
            "the closest idiomatic shape."
        )
    for coupling in couplings:
        if process.id in coupling.producers:
            lines.append(
                f"- Forms a dispatcher/performer pair through **{coupling.queue}**; "
                f"migrate both sides in the same wave."
            )
        elif process.id in coupling.consumers and coupling.producers:
            lines.append(
                f"- Consumes **{coupling.queue}**, which another process in the "
                f"estate feeds; migrate both sides in the same wave."
            )
    return lines


def generate_sdd(
    estate: Estate,
    spec: IntentSpec,
    uplift: UpliftReport,
    log: ProvenanceLog,
    *,
    out_dir: Path,
    force: bool = False,
) -> GeneratedDoc:
    """To-be design document from approved intent plus the uplift analysis."""
    require_approved(spec, log, force=force, actor=ACTOR)
    process = _find_process(estate, spec.process_id)
    if uplift.process_id != process.id:
        raise GenerationError(
            f"uplift report is for process {uplift.process_id}, spec is for {process.id}"
        )
    index = _stage_index(estate)
    graph = build_dependency_graph(estate)
    deps = next(d for d in graph.processes if d.process_id == process.id)
    queues_by_name = {q.name: q for q in estate.queues}
    constants = sorted(config_constants(process))
    values = {d.name: d.initial_value for d in process.data_items}

    lines = [f"# Solution Design Document (to-be): {process.name}", ""]
    lines += _provenance_header(
        spec,
        extra=[
            f"- Uplift analysis: criteria v{uplift.criteria_version}, "
            f"analyzed {uplift.analyzed_at} "
            f"(spec **{uplift.spec_ref.status_at_analysis}** at analysis time)",
        ],
    )

    lines += ["", "## 1. Target architecture", ""]
    lines += _target_architecture(deps, graph.queue_couplings, process)

    lines += ["", "## 2. Queue and configuration design", ""]
    if deps.queues:
        lines += [
            "| Blue Prism queue | UiPath design | Key field | Retries |",
            "|---|---|---|---|",
        ]
        for queue_use in deps.queues:
            queue = queues_by_name.get(queue_use.queue)
            lines.append(
                f"| {queue_use.queue} | Orchestrator queue | "
                f"{queue.key_field if queue else '?'} | "
                f"{queue.max_attempts if queue else '?'} |"
            )
        lines.append("")
    if constants:
        lines += [
            "| Setting | Value to carry over | UiPath home |",
            "|---|---|---|",
        ]
        lines += [
            f"| {name} | {values.get(name)} | Config.xlsx / Orchestrator asset |"
            for name in constants
        ]
    if not deps.queues and not constants:
        lines.append("No queues and no configuration constants to design for.")

    lines += ["", "## 3. Exception taxonomy mapping", ""]
    rows: list[str] = []
    for stage in process.stages:
        exc_type = str(stage.properties.get("exception_type", ""))
        if stage.type == "exception" and "business" in exc_type.lower():
            rows.append(
                f"| Throw at '{stage.name}' | Business Exception | "
                f"`BusinessRuleException`: no retry, route to human follow-up |"
            )
        elif stage.type == "exception":
            rows.append(
                f"| Throw at '{stage.name}' | {exc_type or 'System Exception'} | "
                f"System exception: REFramework retry applies |"
            )
        elif (
            stage.type == "queue_write"
            and "exception" in str(stage.properties.get("action", "")).lower()
        ):
            rows.append(
                f"| '{stage.name}' | Queue item exception | "
                f"Set transaction status Failed (BusinessRuleException), no retry |"
            )
        elif stage.type == "recover":
            rows.append(
                f"| Recover at '{stage.name}' | Recovery block | "
                f"REFramework system-exception retry |"
            )
    if rows:
        lines += ["| Source construct | Blue Prism semantics | UiPath mapping |", "|---|---|---|"]
        lines += rows
    else:
        lines.append("No exception constructs in the source.")

    lines += [
        "",
        "## 4. Per-step decisions (from the uplift analysis)",
        "",
        "Classifications per docs/uplift-criteria.md; candidates are design",
        "inputs for the review below, not decisions already taken.",
        "",
        "| Finding | Classification | Stages | Criteria |",
        "|---|---|---|---|",
    ]
    for finding in uplift.findings:
        lines.append(
            f"| {finding.id} | {finding.classification} | "
            f"{_cites(finding.stage_ids, index)} | {', '.join(finding.criteria)} |"
        )
    candidates = [f for f in uplift.findings if f.classification == "AGENTIC_CANDIDATE"]
    for finding in candidates:
        lines += ["", f"**{finding.id}.** {finding.reasoning}"]

    lines += ["", "## 5. Human build TODO list", ""]
    todo = 1
    for use in deps.objects:
        lines.append(
            f"{todo}. Application interface for **{use.object}** "
            f"({', '.join(use.actions)}): selectors and screen automation are "
            f"not in the release export; build and test the UiPath library "
            f"actions against the real application."
        )
        todo += 1
    for finding in candidates:
        lines.append(
            f"{todo}. Decide finding **{finding.id}** ({', '.join(finding.criteria)}): "
            f"adopt the agentic design with a deterministic fallback, or keep "
            f"the current logic. Record the decision and its reasoning."
        )
        todo += 1
    lines.append(
        f"{todo}. Credentials, robot accounts, and schedules: not present in "
        f"the export; configure in Orchestrator."
    )

    lines += ["", "## 6. Provenance references", ""]
    lines += [
        f"- Intent spec: `{spec.spec_id}`, this directory's `intent-spec.json`",
        f"- Uplift report: this directory's `uplift.json` (criteria v{uplift.criteria_version})",
        f"- Source estate: `{spec.estate_ref.path}` (sha256 `{spec.estate_ref.sha256[:12]}...`)",
        "- Event log: this directory's `provenance.jsonl` (append-only, hash-chained)",
    ]

    lines.append("")
    doc = _write(out_dir, "sdd.md", "\n".join(lines))
    _record_generation(log, "sdd", spec, doc)
    return doc


# --------------------------------------------------------------------------
# Modernization report (estate-wide)
# --------------------------------------------------------------------------


def _wave(score: ComplexityScore, coupled_ids: set[str], waves: dict[str, int]) -> int:
    base = {"low": 1, "medium": 2, "high": 3}[score.band]
    coupled_waves = [waves[pid] for pid in coupled_ids if pid in waves]
    return max([base, *coupled_waves])


def generate_report(
    estate: Estate,
    specs: list[IntentSpec],
    uplift_reports: list[UpliftReport],
    *,
    out_dir: Path,
) -> GeneratedDoc:
    """Estate-wide modernization report: complexity, uplift findings, and
    a migration recommendation per process. Summarizes draft specs as
    drafts; it does not pass the gate because it generates nothing from
    spec content beyond its status."""
    scores = {s.process_id: s for s in score_complexity(estate)}
    graph = build_dependency_graph(estate)
    specs_by_id = {s.process_id: s for s in specs}
    uplift_by_id = {u.process_id: u for u in uplift_reports}

    # Coupled processes migrate together, in the later of their waves.
    coupled: dict[str, set[str]] = {p.id: set() for p in estate.processes}
    for coupling in graph.queue_couplings:
        members = set(coupling.producers) | set(coupling.consumers)
        for member in members:
            if member in coupled:
                coupled[member] |= members - {member}
    waves: dict[str, int] = {}
    for process in sorted(estate.processes, key=lambda p: scores[p.id].score):
        waves[process.id] = _wave(scores[process.id], coupled[process.id], waves)
    for process in estate.processes:  # settle pairs discovered later
        waves[process.id] = _wave(scores[process.id], coupled[process.id], waves)

    lines = [
        "# Modernization report",
        "",
        "Generated by BP2UIP from the estate model, the intent specs, and",
        "the uplift analysis; no LLM involved. Complexity is an ordinal",
        "ranking with documented weights, not an effort estimate. Waves are",
        "a suggested order: lower complexity first, queue-coupled processes",
        "held together in the later of their waves.",
        "",
        f"Estate: {len(estate.processes)} processes, {len(estate.objects)} objects, "
        f"{len(estate.queues)} queues (source parsed {estate.source.parsed_at}).",
        "",
        "| Process | Complexity | Spec | Agentic candidates | Suggested wave |",
        "|---|---|---|---|---|",
    ]
    for process in sorted(estate.processes, key=lambda p: (waves[p.id], scores[p.id].score)):
        score = scores[process.id]
        spec = specs_by_id.get(process.id)
        uplift = uplift_by_id.get(process.id)
        candidates = (
            sum(1 for f in uplift.findings if f.classification == "AGENTIC_CANDIDATE")
            if uplift
            else None
        )
        lines.append(
            f"| {process.name} | {score.score} ({score.band}) | "
            f"{spec.status if spec else 'none'} | "
            f"{candidates if candidates is not None else 'not analyzed'} | "
            f"{waves[process.id]} |"
        )

    for process in sorted(estate.processes, key=lambda p: (waves[p.id], scores[p.id].score)):
        score = scores[process.id]
        spec = specs_by_id.get(process.id)
        uplift = uplift_by_id.get(process.id)
        lines += ["", f"## {process.name}", ""]
        lines.append(
            f"Complexity {score.score} ({score.band}): {score.logic_stage_count} logic "
            f"stages, {score.decision_count} decisions, branching depth "
            f"{score.branching_depth}, {score.exception_construct_count} exception "
            f"construct(s)."
        )
        if spec is None:
            lines.append(
                "No intent spec; extract and review one before generating "
                "documents for this process."
            )
            continue
        if spec.status != "approved":
            lines.append(
                "Spec is a draft awaiting review; PDD/SDD generation will "
                "refuse to run until it is approved."
            )
        if uplift is None:
            lines.append("No uplift analysis; run `bp2uip analyze`.")
            continue
        gates = sum(1 for f in uplift.findings if f.classification == "HUMAN_GATE")
        candidates = [f for f in uplift.findings if f.classification == "AGENTIC_CANDIDATE"]
        lines.append(
            f"Uplift: {len(candidates)} agentic candidate(s), {gates} human "
            f"gate(s) to preserve, remainder deterministic."
        )
        for finding in candidates:
            lines.append(f"- {finding.id} ({', '.join(finding.criteria)}): {finding.reasoning}")
        partners = coupled[process.id]
        if partners:
            names = ", ".join(p.name for p in estate.processes if p.id in partners)
            lines.append(
                f"Recommendation: migrate in wave {waves[process.id]}, together "
                f"with {names} (queue-coupled)."
            )
        else:
            lines.append(f"Recommendation: migrate in wave {waves[process.id]}.")

    lines.append("")
    return _write(out_dir, "modernization-report.md", "\n".join(lines))
