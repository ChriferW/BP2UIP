def generate_pdd(
    estate: Estate,
    spec: IntentSpec,
    log: ProvenanceLog,
    *,
    out_dir: Path,
    force: bool = False,
) -> GeneratedDoc:
    """As-is process document from parser output plus approved intent."""
    require_approved(spec, log, force=force, actor=ACTOR)
    process = _find_process(estate, spec.process_id)
    index = _stage_index(estate)
    deps = next(d for d in build_dependency_graph(estate).processes if d.process_id == process.id)
    queues_by_name = {q.name: q for q in estate.queues}

    lines = [f"# Process Definition Document (as-is): {process.name}", ""]
    lines += _provenance_header(spec)

    lines += ["", "## 1. Purpose", "", spec.purpose.text, ""]
    lines.append(f"Evidence: {_cites(spec.purpose.citations, index)}")

    lines += ["", "## 2. Trigger and workload", ""]
    reads = [s for s in process.stages if s.type == "queue_read"]
    start_inputs = next(
        (s.properties.get("inputs") or [] for s in process.stages if s.type == "start"), []
    )
    if reads:
        for stage in reads:
            queue_name = str(stage.properties.get("queue_name", ""))
            queue = queues_by_name.get(queue_name)
            detail = (
                f" (key field {queue.key_field}, max {queue.max_attempts} attempts)"
                if queue is not None
                else ""
            )
            lines.append(
                f"- Transactional: works items from queue **{queue_name}**{detail}, "
                f"read at '{stage.name}'."
            )
    if start_inputs:
        params = ", ".join(f"{p.get('name')} ({p.get('type')})" for p in start_inputs)
        lines.append(f"- Invoked with parameters: {params}.")
    if not reads and not start_inputs:
        lines.append(
            "- No queue reads and no start parameters in the source; the "
            "trigger is external to the export (scheduler or manual run)."
        )

    lines += ["", "## 3. Inputs", ""]
    if spec.inputs:
        lines += ["| Input | Description | Source | Evidence |", "|---|---|---|---|"]
        lines += [
            f"| {i.name} | {i.description} | {i.source} | {_cites(i.citations, index)} |"
            for i in spec.inputs
        ]
    else:
        lines.append("None identified in the reviewed spec.")

    lines += ["", "## 4. Outputs", ""]
    if spec.outputs:
        lines += ["| Output | Description | Destination | Evidence |", "|---|---|---|---|"]
        lines += [
            f"| {o.name} | {o.description} | {o.destination} | {_cites(o.citations, index)} |"
            for o in spec.outputs
        ]
    else:
        lines.append("None identified in the reviewed spec.")

    lines += [
        "",
        "## 5. Current-state flow",
        "",
        "Stages in link order from Start, true branches first; stages",
        "reachable only through exception recovery follow at the end.",
        "",
        "| # | Stage | Type | Detail | Reached |",
        "|---|---|---|---|---|",
    ]
    for i, (stage, annotation) in enumerate(_flow_walk(process), start=1):
        lines.append(
            f"| {i} | {stage.name} | {stage.type} | {_stage_detail(stage)} | {annotation} |"
        )

    lines += ["", "## 6. Business rules", ""]
    if spec.business_rules:
        for rule in spec.business_rules:
            lines += [
                f"**{rule.id}.** {rule.statement}",
                "",
                f"Evidence: {_cites(rule.citations, index)}",
                "",
            ]
        lines.pop()
    else:
        lines.append("None identified in the reviewed spec.")

    lines += ["", "## 7. Current exception handling", ""]
    if spec.exception_semantics:
        lines += ["| When | Current handling | Evidence |", "|---|---|---|"]
        lines += [
            f"| {s.condition} | {s.current_handling} | {_cites(s.citations, index)} |"
            for s in spec.exception_semantics
        ]
    else:
        lines.append("None identified in the reviewed spec.")
    for block in process.exception_blocks:
        handler = index.get(block.handler_stage, block.handler_stage)
        lines += [
            "",
            f"Structural: an exception block covers {len(block.covers_stages)} "
            f"stage(s); exceptions inside it are handled at {handler}.",
        ]

    lines += ["", "## 8. Systems and dependencies", ""]
    if deps.objects:
        for use in deps.objects:
            lines.append(f"- **{use.object}**: {', '.join(use.actions)}")
    if deps.queues:
        for queue_use in deps.queues:
            lines.append(
                f"- Queue **{queue_use.queue}**: {queue_use.reads} read(s), "
                f"{queue_use.adds} add(s), {queue_use.dispositions} disposition(s)"
            )
    if deps.subprocesses:
        for sub in deps.subprocesses:
            lines.append(f"- Subprocess: {sub}")
    if not (deps.objects or deps.queues or deps.subprocesses):
        lines.append("No object, queue, or subprocess dependencies in the source.")

    lines += ["", "## 9. Human touchpoints", ""]
    if spec.human_touchpoints:
        for touchpoint in spec.human_touchpoints:
            lines += [
                f"- {touchpoint.description}",
                f"  Evidence: {_cites(touchpoint.citations, index)}",
            ]
    else:
        lines.append("None identified in the reviewed spec.")

    constants = sorted(config_constants(process))
    values = {d.name: d.initial_value for d in process.data_items}
    lines += ["", "## 10. Configuration", ""]
    if constants:
        lines += ["| Setting | Current value |", "|---|---|"]
        lines += [f"| {name} | {values.get(name)} |" for name in constants]
        lines += [
            "",
            "Configuration means a data item with an initial value that no",
            "stage writes; values shown are the current values in the source.",
        ]
    else:
        lines.append("No configuration constants in the source.")

    lines.append("")
    doc = _write(out_dir, "pdd.md", "\n".join(lines))
    _record_generation(log, "pdd", spec, doc)
    return doc


